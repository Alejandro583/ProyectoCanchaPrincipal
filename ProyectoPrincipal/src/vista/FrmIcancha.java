    public void realizarOperacion()
    {
        oModeloCancha = new modeloCancha();
        if(opcion.equals("GUARDAR"))
        {
            oModeloCancha.setNombre(txtNombre.getText());
            oModeloCancha.setDisponible(txtDisponible.getText());
            oModeloCancha.setMantenimiento(txtMantenimiento.getText());
            oModeloCancha.setPrecio(Float.parseFloat(txtPrecio.getText()));
            boolean resultado = oAbmCancha.agregarCancha(oModeloCancha);
            if(resultado)
            {
                JOptionPane.showMessageDialog(null, "Cancha Agregado correctamente");
                this.setVisible(false);
            }else
            {
                JOptionPane.showMessageDialog(null, "Error al agregar la Cancha");
            }
            
        }
        else
        {
            oModeloCancha.setNombre(txtNombre.getText());
            oModeloCancha.setDisponible(txtDisponible.getText());
            oModeloCancha.setMantenimiento(txtMantenimiento.getText());
            oModeloCancha.setPrecio(Float.parseFloat(txtPrecio.getText()));
            oModeloCancha.setId_cancha(Integer.parseInt(txtCodigo.getText()));
            boolean resultado = oAbmCancha.modificarCancha(oModeloCancha);
            if(resultado)
            {
                JOptionPane.showMessageDialog(null, "Cancha Modificado correctamente");
                this.setVisible(false);
            }else
            {
                JOptionPane.showMessageDialog(null, "Error al Modificar la Cancha");
            }
        }
    }


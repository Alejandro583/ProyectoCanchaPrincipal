    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        if(oAbmCliente.clienteExiste(txtCI.getText())== null)
        {
            oModeloCliente = new modeloCliente();
            oModeloCliente.setCi(txtCI.getText());
            oModeloCliente.setEstado(1);
            oModeloCliente.setNombre(txtNombre.getText());
            oModeloCliente.setTelefono(txtTelefono.getText());
            oAbmCliente.agregarCliente(oModeloCliente);
        }
        oModeloCliente = oAbmCliente.clienteExiste(txtCI.getText());
        //int idCliente = oAbmCliente.obtenerIdClientePorCedula(oModeloCliente.getCi());
        String horario = cbxHorarios1.getSelectedItem().toString();
        // Separar en dos partes: Inicio y Fin
        String[] partesHorario = horario.split("-");
        
        // Quitar espacios extra
        String horarioInicio = partesHorario[0].trim();
        String horarioFin = partesHorario[1].trim();
        // Crear y completar el modeloReserva
        modeloReserva oModeloReserva = new modeloReserva();
        oModeloReserva.setFk_cancha(oAbCancha.obtenerIdCanchaPorNombre(cbxCancha.getSelectedItem().toString())); // OJO: aquí quizás quieras usar getSelectedItem() o un ID real
        oModeloReserva.setFk_cliente(oModeloCliente.getId_cliente());
        oModeloReserva.setHorario_inicio(horarioInicio);
        oModeloReserva.setHorario_fin(horarioFin);
        oModeloReserva.setFechaReserva(cbxFecha.getSelectedItem().toString());
        oModeloReserva.setObs(txtObservacion.getText());
        oAbmReserva.agregarReserva(oModeloReserva);
        JOptionPane.showMessageDialog(null, "Reserva Guardado correctamente");
        
        //actualizamos el horario
        
        cbxHorarios1.setModel(oAbmReserva.cargarHorarios(1, cbxFecha.getSelectedItem().toString()));
    }//GEN-LAST:event_jButton4ActionPerformed


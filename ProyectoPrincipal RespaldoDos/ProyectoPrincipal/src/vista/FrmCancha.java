    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = grillaCancha.getSelectedRow();
        oModeloCancha = new modeloCancha();
        if (filaSeleccionada != -1) { // si hay una fila seleccionada
            int  id = Integer.parseInt(grillaCancha.getValueAt(filaSeleccionada, 0).toString()); // columna 0 = primera columna
            oModeloCancha.setId_cancha(id);
            oModeloCancha = oAbmCancha.canchaExiste(oModeloCancha);
            boolean resultado = oAbmCancha.eliminarCancha(oModeloCancha);
            if(resultado)
            {
                JOptionPane.showMessageDialog(null, "Cancha Eliminado correctamente");
                actualizarTabla();
            }else
            {
                JOptionPane.showMessageDialog(null, "Error al Eliminar");
            }
            
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    public void actualizarTabla()
    {
        grillaCancha.setModel(oAbmCancha.cargarTabla(""));
    }
